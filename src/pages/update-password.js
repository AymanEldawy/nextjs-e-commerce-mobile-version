import React, { useContext, useState } from 'react';
import Head from 'next/head';
import { PrimaryButton } from './../components/Global/PrimaryButton/PrimaryButton';
import { fetchWord } from '@/lang/fetchWord';
import { LanguageContext } from './../context/LangContext';
import { InputField } from './../components/Forms/InputField';
const UpdatePassword = () => {
  const { lang } = useContext(LanguageContext);
  const [password, setPassword] = useState('');
  const [confirmPassword, setConfirmPassword] = useState('');
  const handleSubmit = (e) => {
    e.preventDefault();
  };
  return (
    <>
      <Head>
        <title>Update Password</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="mobile-container">
        <div className="flex gap-4 flex-col mt-12">
          <div className="">
            <h1 className="text-2xl mb-4 font-semibold">{fetchWord('Update Password', lang)}</h1>
            <p className="text-sm mb-4 text-[#00000099]">
              {fetchWord('Please enter your new password and confirm it', lang)}
            </p>
          </div>
          <form onSubmit={handleSubmit} className="mt-6">
            <InputField
              style={{
                border: '2px solid #E5E5E5',
                background: '#0000000D',
                padding: '12px',
              }}
              label={fetchWord('password_label', lang)}
              type="password"
              name="password"
              placeholder={fetchWord('password_label', lang)}
              value={password}
              handleChange={(e) => setPassword(e.target.value)}
            />
            <InputField
              style={{ border: '2px solid #E5E5E5', padding: '12px' }}
              label={fetchWord('confirm_password_label', lang)}
              type="password"
              name="confirmPassword"
              placeholder={fetchWord('confirm_password_placeholder', lang)}
              value={confirmPassword}
              handleChange={(e) => setConfirmPassword(e.target.value)}
            />
          </form>

          <PrimaryButton
            text={fetchWord('save', lang)}
            classes=" w-full rounded-lg !py-4 capitalize"
          />
        </div>
      </div>
    </>
  );
};
export default UpdatePassword;
